package cmd

import (
	"os"
	"path/filepath"
	"text/template"

	"github.com/spf13/cobra"

	commontemplate "github.com/kumahq/kuma/v3/tools/common/template"
	"github.com/kumahq/kuma/v3/tools/policy-gen/generator/pkg/parse"
)

func newHelpers(rootArgs *args) *cobra.Command {
	cmd := &cobra.Command{
		Use:   "helpers",
		Short: "Generate helper funcs for the policy",
		Long:  "Generate helper funcs for the policy.",
		RunE: func(cmd *cobra.Command, _ []string) error {
			policyName := filepath.Base(rootArgs.pluginDir)
			policyPath := filepath.Join(rootArgs.pluginDir, "api", rootArgs.version, policyName+".go")
			if _, err := os.Stat(policyPath); err != nil {
				return err
			}

			pconfig, err := parse.Policy(policyPath)
			if err != nil {
				return err
			}

			if !pconfig.IsPolicy {
				return nil
			}

			// GetPolicyItem is only generated for policies without from/to/rules lists;
			// those policies implement PolicyItem directly on the root spec struct
			generateGetPolicyItem := !pconfig.HasFrom && !pconfig.HasTo && !pconfig.HasRules
			needsCoreModel := pconfig.HasFrom || pconfig.HasTo || generateGetPolicyItem

			outPath := filepath.Join(filepath.Dir(policyPath), "zz_generated.helpers.go")
			return commontemplate.GoTemplate(helpersTemplate, map[string]any{
				"name":                  pconfig.Name,
				"version":               pconfig.Package,
				"generateTo":            pconfig.HasTo,
				"generateFrom":          pconfig.HasFrom,
				"generateRules":         pconfig.HasRules,
				"ruleHasMatches":        pconfig.RuleHasMatches,
				"skipGetDefault":        pconfig.SkipGetDefault,
				"generateGetPolicyItem": generateGetPolicyItem,
				"needsCoreModel":        needsCoreModel,
			}, outPath)
		},
	}

	return cmd
}

var helpersTemplate = template.Must(template.New("missingkey=error").Parse(
	`
// Generated by tools/policy-gen. DO NOT EDIT.
// Run "make generate" to update this file.

package {{.version}}

import (
	common_api "github.com/kumahq/kuma/v3/api/common/v1alpha1"{{ if .needsCoreModel }}
	core_model "github.com/kumahq/kuma/v3/pkg/core/resources/model"{{ end }}{{ if .generateRules }}
	"github.com/kumahq/kuma/v3/pkg/plugins/policies/core/rules/inbound"{{ end }}{{ if or .generateFrom .generateTo .generateRules }}
	"github.com/kumahq/kuma/v3/pkg/util/pointer"{{ end }}
)

func (x *{{.name}}) GetTargetRef() common_api.TargetRef {
	return x.TargetRef.ToTargetRef()
}

{{ if .generateFrom }}

func (x *From) GetTargetRef() common_api.TargetRef {
	return x.TargetRef
}
{{ if not .skipGetDefault }}

func (x *From) GetDefault() interface{} {
	return x.Default
}
{{- end }}

func (x *{{.name}}) GetFromList() []core_model.PolicyItem {
	var result []core_model.PolicyItem
	for _, itm := range pointer.Deref(x.From) {
		item := itm
		result = append(result, &item)
	}
	return result
}
{{- end }}
{{ if .generateTo }}

func (x *To) GetTargetRef() common_api.TargetRef {
	return x.TargetRef.ToTargetRef()
}
{{ if not .skipGetDefault }}

func (x *To) GetDefault() interface{} {
	return x.Default
}
{{- end }}

func (x *{{.name}}) GetToList() []core_model.PolicyItem {
	var result []core_model.PolicyItem
	for _, itm := range pointer.Deref(x.To) {
		item := itm
		result = append(result, &item)
	}
	return result
}
{{- end }}

{{ if .generateRules }}
func (x *Rule) GetDefault() interface{} {
	return x.Default
}

func (x *Rule) GetMatches() []common_api.Match {
{{- if .ruleHasMatches }}
	return pointer.DerefOr(x.Matches, []common_api.Match{})
{{- else }}
	return []common_api.Match{}
{{- end }}
}

func (x *{{.name}}) GetRules() []inbound.RuleEntry {
	var result []inbound.RuleEntry
	for _, itm := range pointer.Deref(x.Rules) {
		item := itm
		result = append(result, &item)
	}
	return result
}
{{- end }}

{{ if .generateGetPolicyItem}}
{{ if not .skipGetDefault }}

func (x *{{.name}}) GetDefault() interface{} {
	return x.Default
}
{{- end }}

func (x *{{.name}}) GetPolicyItem() core_model.PolicyItem {
	return &policyItem{
		{{.name}}: x,
	}
}

// policyItem is an auxiliary struct with the implementation of the GetTargetRef() to always return empty result
type policyItem struct {
	*{{.name}}
}

var _ core_model.PolicyItem = &policyItem{}

func (p *policyItem) GetTargetRef() common_api.TargetRef {
	return common_api.TargetRef{Kind: common_api.Mesh}
}
{{- end }}
`))
