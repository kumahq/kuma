package sync

import (
	"bytes"
	"testing"

	common_api "github.com/kumahq/kuma/v3/api/common/v1alpha1"
	v1 "k8s.io/apimachinery/pkg/apis/meta/v1"

	meshidentity_api "github.com/kumahq/kuma/v3/pkg/core/resources/apis/meshidentity/api/v1alpha1"
	"github.com/kumahq/kuma/v3/pkg/test/resources/builders"
	test_model "github.com/kumahq/kuma/v3/pkg/test/resources/model"
)

func TestHashMeshIdentityUsesXDSHash(t *testing.T) {
	first := builders.MeshIdentity().WithBundledAutoGenerated().Build()
	first.Meta.(*test_model.ResourceMeta).Version = "1"
	first.Status = &meshidentity_api.MeshIdentityStatus{
		Conditions: []common_api.Condition{{
			Type:   meshidentity_api.ReadyConditionType,
			Status: v1.ConditionTrue,
			Reason: "Ready",
		}},
	}

	second := builders.MeshIdentity().WithBundledAutoGenerated().Build()
	second.Meta.(*test_model.ResourceMeta).Version = "2"
	second.Status = &meshidentity_api.MeshIdentityStatus{
		Conditions: []common_api.Condition{{
			Type:   meshidentity_api.ReadyConditionType,
			Status: v1.ConditionFalse,
			Reason: "PartiallyReady",
		}},
	}

	if !bytes.Equal(hashMeshIdentity(first), hashMeshIdentity(second)) {
		t.Fatal("expected MeshIdentity watchdog hash to ignore version and status-only changes")
	}
}
